# Defining an integer

myint = 7
print(myint)

# Float

myfloat = 7.0
print(myfloat)
myfloat = float(7)
print(myfloat)

# String

mystring = 'hello'
print(mystring)
mystring = "hello" # Better if you plan to use apostrophes
print(mystring)

# Simple operators

one = 1
two = 2
three = one + two
print(three)

hello = "hello"
world = "world"
helloworld = hello + " " + world
print(helloworld)
